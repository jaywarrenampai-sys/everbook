/** Calculate price for a given quantity using the pricing tiers */
export function getPriceForQuantity(qty: number): { unit: number; total: number; saved: number } {
  const tiers = pricing.tiers;
  // Find exact tier match first
  const exactTier = tiers.find((_, i) => i + 1 === qty);
  if (exactTier) {
    return { unit: exactTier.perBook, total: exactTier.price, saved: exactTier.originalPrice - exactTier.price };
  }
  // Beyond tier 3: use tier-3 per-book price
  const bestTier = tiers[tiers.length - 1];
  const total = bestTier.perBook * qty;
  const originalTotal = bestTier.originalPrice / 3 * qty;
  return { unit: bestTier.perBook, total, saved: Math.round(originalTotal - total) };
}

// Helper: format Baht amounts
export function formatBaht(amount: number): string {
  return `฿${amount.toLocaleString("th-TH")}`;
}
